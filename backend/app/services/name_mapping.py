# Combined map for lookup
ALL_NAME_MAP = {**UFC_NAME_MAP, **RIZIN_NAME_MAP}


def _to_romaji(text: str) -> str:
    """Convert Japanese text to romaji using pykakasi."""
    try:
        import pykakasi
        kks = pykakasi.kakasi()
        result = kks.convert(text)
        parts = []
        for item in result:
            hepburn = item.get("hepburn", "")
            if hepburn:
                parts.append(hepburn.capitalize())
        return " ".join(parts)
    except Exception:
        return text


def translate_name(japanese_name: str) -> str | None:
    """Translate a Japanese fighter name to English.
    First checks manual mapping, then falls back to romaji conversion."""
    name = japanese_name.strip()

    # 1. Exact match in mapping
    if name in ALL_NAME_MAP:
        return ALL_NAME_MAP[name]

    # 2. Partial match in mapping
    for jp, en in ALL_NAME_MAP.items():
        if name in jp or jp in name:
            return en

    # 3. Romaji conversion fallback
    romaji = _to_romaji(name)
    if romaji and romaji != name:
        return romaji

    return None


def suggest_japanese(query: str, limit: int = 10) -> list[dict]:
    """Suggest fighters from Japanese name input.
    Checks manual mapping first, then generates romaji suggestions."""
    query = query.strip()
    results = []
    seen = set()

    # 1. Search manual mapping
    for jp, en in ALL_NAME_MAP.items():
        if query in jp:
            if en not in seen:
                seen.add(en)
                results.append({
                    "name": en,
                    "nickname": jp,
                    "record": "",
                    "weight_class": "",
                })
        if len(results) >= limit:
            return results

    # 2. If no mapping results, convert to romaji for Sherdog search
    # Return empty so the caller falls through to Sherdog search with romaji
    return results


def get_romaji_query(query: str) -> str | None:
    """Convert Japanese query to romaji for external search."""
    romaji = _to_romaji(query)
    if romaji and romaji != query:
        return romaji
    return None
